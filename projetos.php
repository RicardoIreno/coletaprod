class ListProjetos {
  static function listAll($data) {
    $arr = json_decode($data);
    $projetos = $arr -> projetos ;

    // var_dump($projetos );
   
    foreach( $projetos as $p ) {

      $p -> concluido === "true" ? $concluido = "Projeto Concluído" : $concluido = "";

      SList::genericItem(
        $type = 'ppg',
        $itemName = $p -> nome,
        $itemNameLink = '',
        $itemInfoA = substr($p -> descricao, 0, 100) . '...',
        $itemInfoB = '<i>Financiado por:</i> ' . $p -> financiadores,
        $itemInfoC = '<i>Coordenação:</i> ' . $p -> coordenacao,
        $itemInfoD = '<i>Integrantes:</i> ' . implode(', ', $p -> integrantes),
        $itemInfoE = $concluido,
        $yearStart = $p -> ano_inicial,
        $yearEnd = $p -> ano_final
      );
    }
  }
}
        
